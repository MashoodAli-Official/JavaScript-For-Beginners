// Elements accessing in arrays, As we know that the array is indexed based data structure. By using the index number we can access the elements of the array. 
// The index number is starting from 0 and so on.

const studentNames = ['Harry Potter', 'Hermione Granger', 'Ron Weasley' ];
// For accessing the studentNames array elements we can use the index number. Like in given below example the index number of the first element is 0.
console.log(studentNames[0]); // Harry Potter