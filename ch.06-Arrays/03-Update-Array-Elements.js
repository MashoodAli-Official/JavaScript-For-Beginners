//We can update any element of an array by using the index number and assignment operator.

let studentNames = ["John", "Jane", "Alex", "Sara", "Tom"];
console.log("Before updating the array",studentNames); //Output: [ 'John', 'Jane', 'Alex', 'Sara', 'Tom' ]
//updating the element at index 3
studentNames[3] = "Sarah";
console.log("After updating the array",studentNames); //Output: [ 'John', 'Jane', 'Alex', 'Sarah', 'Tom' ]