// Template Literals are sort of way to write a string in JavaScript. For this we have to use backticks (`) instead of single quotes ('') or double quotes ("").
// By using Template Literals we can write multi-line strings and also we can use expressions inside the string.

const name = 'Harry Potter';
const age = 20;
console.log(`My name is ${name} and i am ${age} years old.`);