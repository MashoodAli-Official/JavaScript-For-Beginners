// If Else statement in JavaScript, We are having 2 conditions, if the condition is true then it will execute the first block of code, if the condition is false then it will execute the second block of code.

//First Example
let x = 1;
if (x == 2){
    console.log("x is 1");
} else{
    console.log('x is not 1');
};
// x is not 1

// Second Example
let y = false;
if (y == true){
    console.log("y is True");
}else{
    console.log('y is not False');
};