// Assignment operator are used to assign values to variables.

// Assignment operator
let x = 10;
console.log("Assignment operator = ",x); // 10

// Addition assignment operator
x += 5; // x = x + 5
console.log("Addition += assignment operator ",x); // 15  

// Subtraction assignment operator
x -= 3; // x = x - 3
console.log("Subtraction -= assignment operator ",x); // 12

// Multiplication assignment operator
x *= 2; // x = x * 2
console.log("Multiplication *= assignment operator ", x); // 24

// Division assignment operator
x /= 4; // x = x / 4
console.log("Division /= assignment operator ", x); // 6

// Modulus assignment operator
x %= 3; // x = x % 3
console.log("Modulus %= assignment operator ", x); // 0

// Exponentiation assignment operator
x **= 2; // x = x ** 2
console.log("Exponentiation **= assignment operator ", x); // 0

// Left shift assignment operator
x <<= 1; // x = x << 1
console.log("Left shift <<= assignment operator ", x); // 0

// Right shift assignment operator
x >>= 1; // x = x >> 
console.log("Right shift >>= assignment operator ", x);

// Unsigned right shift assignment operator
x >>>= 1; // x = x >>> 1
console.log("Unsigned right shift >>>= assignment operator ", x);

// Bitwise AND assignment operator
x &= 5; // x = x & 5
console.log("Bitwise AND &= assignment operator ", x);

// Bitwise OR assignment operator
x |= 3; // x = x | 3
console.log("Bitwise OR |= assignment operator ", x);

// Bitwise XOR assignment operator
x ^= 6; // x = x ^ 6
console.log("Bitwise XOR ^= assignment operator ", x);

// Logical AND assignment operator
x &&= 2; // x = x && 2
console.log("Logical AND &&= assignment operator ", x);

// Logical OR assignment operator
x ||= 1; // x = x || 1
console.log("Logical OR ||= assignment operator ", x);

// Logical nullish assignment operator
x ??= 10; // x = x ?? 10
console.log("Logical nullish assignment operator ", x); // 1