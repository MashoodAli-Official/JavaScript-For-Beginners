// In javascript there are total 8 data types.
// 7 of them are primitive data types and 1 is object data type.

// Primitive Data Types
// 1. Number
// 2. String
// 3. Boolean
// 4. Undefined
// 5. Null
// 6. Symbol
// 7. BigInt

// Object Data Type
// 1. Object

// Number
let a = 10;
console.log(a);
console.log(typeof a);

// String
let b = "Hello";
console.log(b);
console.log(typeof b);

// Boolean
let c = true;
console.log(c);
console.log(typeof c);

// Undefined
let d;
console.log(d);
console.log(typeof d);

// Null
let e = null;
console.log(e);
console.log(typeof e);

// Symbol
let f = Symbol("Hello");
console.log(f);
console.log(typeof f);

// BigInt
let g = BigInt(10);
console.log(g);
console.log(typeof g);

// Object
let h = {
    name: "John",
    age: 30
};
console.log(h);
console.log(typeof h);