// Common JavaScript methods by category:
// Array Methods
array.push()      // Add element to end
array.pop()       // Remove last element
array.shift()     // Remove first element
array.unshift()   // Add element to start
array.splice()    // Add/remove elements
array.slice()     // Extract portion of array
array.concat()    // Merge arrays
array.join()      // Convert array to string
array.map()       // Transform elements
array.filter()    // Filter elements
array.reduce()    // Reduce to single value
array.forEach()   // Loop through elements
array.find()      // Find matching element
array.includes()  // Check if element exists
array.indexOf()   // Get element index

// String Methods
string.length     // Get string length
string.charAt()   // Get char at position
string.slice()    // Extract substring
string.split()    // Convert to array
string.replace()  // Replace text
string.trim()     // Remove whitespace
string.toUpperCase() // Convert to uppercase
string.toLowerCase() // Convert to lowercase

// Number Methods
Number.parseInt() // Convert to integer
Number.parseFloat() // Convert to float
number.toFixed()  // Format decimals
Math.round()      // Round number
Math.floor()      // Round down
Math.ceil()       // Round up
Math.random()     // Get random number

// Object Methods
Object.keys()     // Get property names
Object.values()   // Get property values
Object.entries()  // Get key-value pairs
Object.assign()   // Copy properties
Object.freeze()   // Make immutable
Object.seal()     // Prevent new properties